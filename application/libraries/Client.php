    /**
     * age
     *  Retourne l'age du client en fonction de sa date de naissance
     * @return int
     */
    public function age() :int { 
        $age = date('Y') - intval($this->birthday); 
        if (date('md') < date('md', strtotime(intval($this->birthday)))) { 
            return $age - 1; 
        } 
        return $age; 
    } 

